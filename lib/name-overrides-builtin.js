module.exports = function nameOverridesBuiltin(name, objectType)
{
        if (name === '__proto__')
        {
                // In case Object.create is a polyfill
                return true;
        }

        var value = table[name];
        if (!value)
        {
                return false;
        }

        if (objectType === 'HTMLDocument' || objectType === 'document')
        {
                return !!(value & isHTMLDocument);
        }

        if (objectType === 'HTMLFormElement' || objectType === 'form')
        {
                return !!(value & isHTMLFormElement);
        }

        return true;
};

module.exports.list = function(objectType)
{
        var list = Object.keys(table);

        if (objectType === 'HTMLDocument' || objectType === 'document')
        {
                return list.filter(function(name)
                {
                        return !!(table[name] & isHTMLDocument);
                });
        }

        if (objectType === 'HTMLFormElement' || objectType === 'form')
        {
                return list.filter(function(name)
                {
                        return !!(table[name] & isHTMLFormElement);
                });
        }

        return list;
};
